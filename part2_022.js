// and 논리 연산자 &&
console.log(true && true);
console.log(true && false);
console.log("문장" == "문장" && 5 == "5");

// or 논리 연산자 ||
console.log(true || false);
console.log(true || true);
console.log(false || false);

// not 논리 연산자 !
console.log(!true);
console.log(!false);
console.log(!5);
console.log(!"문장");
console.log(!!5);
